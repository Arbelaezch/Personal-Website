          <p>
			 
          
				Iron Man deserves an eight and here's why: not only did this film kick off THE most commercially successful film franchise of all time, it also reinvented an entire genre and still, over ten years and more than twenty films later, remains an enjoyably fun, action packed ride led by one of the most charasmatic, well cast lead actors you could possibly ask for. That was a mouthful so lets break it down. Also, throughout most of these MCU reviews I could probably spend all my time defending them and their overreliance on CGI and "shallow" action, but instead I will save that discussion for my final review of phase one through three, because that is where I feel these films deserve real respect.
				
				<br><br>

				To start this review, lets go back to the May of 2008. The state of the superhero film genre has turned to mostly a hot dumpster fire with Spider-man 3, Fantastic Four 2, and X-Men 3 over the past two years. <em>Shudder</em> The lone bastion of hope for the genre... Batman Begins from 2005 (Dark Knight is still a few months away). A dark, gritty, realistic take on the superhero genre that almost strips all of the "super" out of it. This is likely in an attempt to break away from the onslaught of goofy hero films that were coming out around the time. Enter Iron Man, a funny action packed superhero film that is both firmly grounded in reality (featuring the war in Afganistan) and also takes audiences to a slightly exaggerated reality where it's okay to be 'super.' Batman Begins and The Dark Knight have gained much recognition for re-inventing the superhero genre, and yet Iron Man established a style that has not only stuck, but also defined an entire decade of film and entertainment. Don't @ me.

				<br><br>

				The story is nothing special, but in a way that's exactly what the genre needed. Marvel knew that with Iron Man they needed people to identify with it and have it feel familiar. Not just because of audiences tumultuous relationship with hero flicks, but also because the character of Iron Man was a complete NOBODY. I remember sitting in the theatre before it started, thinking who tf is this guy. Now I would love for you to find me a single human being that doesn't know his name. Of course the character's popularity, and I think an argument could be made that this entire film's popularity, is a direct result of the magnetic personality of Robert Downey Jr. This man single handedly lifts the plot of this film from a fun yet generic action flick to a wildly enjoyable time. Its because of this man that this film has endless rewatchability. And it's very possible this man is the whole reason these films were given a chance to become what they did.

				<br><br>

				So these factors taken together, the film's ingenuity, legacy, and incredibly enjoyable ride, are why I consider Iron Man to be worth an eight. It's not the pinnacle of filmmaking, but it's a damn good time.




				


            
            
