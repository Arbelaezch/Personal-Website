          <p>
			 
				Man what a great way to start the Monsterverse. Kong: Skull Island is technically the second monster film in Warner Bros' Monster universe, though they have since departed quite radically from the style of Gareth Edwards' Godzilla in 2014. Now if there's anything I like in films, it's the style of the Vietnam war when captured on screen, and giant cool monsters fighting.
            
            <br><br>


				The monsters are cool. Thankfully, there's quite a few really cool monsters located on Skull Island, all of which I was quite down for. The one I enjoyed the most that I wish we could have seen more is the long stick legged monster that speared soldiers through their mouths.

				<br><br>

				The Vietnam style was wild and the sequence with the helicopters flying in over some Creedance, or was it Rolling Stones? Who cares. It was wild and I loved it. Whoever had the idea to set this during the Vietnam era is a genius. The style points don't stop there as there was some genuinely enjoyable cinematography going on in here. I caught myself a few times admiring the shot composition and beautiful landscapes. To add, I was very impressed to discover they actually shot on location in Hawaii and Vietnam. Well done WB.

				<br><br>

				And now the bad. Boy oh boy does the dialogue suck. I think the director had the right idea making the characters basically characatures. The insane army colonol played by Samuel L. Jackson, the wacky scientist John Goodman, the walking hero Tom Hiddelston, I think these characters could have been greatly entertaining. For the most part though, I think there was a mix of writing that was taking itself too seriously, and actors that were taking it too seriously. Like I think all of the cast needed the same energy that John C. Riley and Samuel L. brought. I know I may be in the minority here, but if I have the choice of a cast of generic acting/writing versus the cheese turned up just a little more, I will ride that cheese all the way to the bank. Especially in a movie like Kong.

				<br><br>

				Overall, I really enjoyed Kong and will likely find myself throwing it on in the background every once and a while just for kicks. Though I will obviously be skipping most of the character stuff.



            
            
          </p>  
