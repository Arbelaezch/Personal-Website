          <p>
			 
			Oh boy oh boy. I really wanted to enjoy this movie, I really did. I typically enjoy disaster director Roland Emmerich's pictures, but this unfortunately did not do it for me. Going in expecting a movie as fun as the original Independence Day is unrealistic, but this sequel hits short on just about every metric, except for VFX.

			<br><br>

			So lets start with the positives, the movie looks amazing. The effects are realistic and have weight. When entire cities are being upturned, I was in awe at how good everything looked. I do have to admit there is a special place in my heart for watching massive destructive events occuring on screen, and this film certainly delivers. If that is all you're looking for, you will definitely be satisfied.

			<br><br>

			Beyond that, the performances are fine. Goldblum is nice, but not used nearly enough. I do also like how there were many deaths among the secondary cast members, though not enough to ever make me thing the primary cast was in danger. 

			<br><br>

			Where things really break down for me is the acting and the writing. Now you don't go into a film like this expecting an award winning script, but I do still expect the plot to make sense, and MAYBE even make me care about a character or two. I didn't end up liking anyone, possibly because the first sentence out of everyone's mouths was exposition for the character and their primary character trait and it just completely distracted me. I don't think any one character got enough screen time to really make me care because there were so many new additions, with so many of them being useless to the plot. First off, you will never be able to convince me that the Chinese girl and that one guys love for her was simply added late in production to appeal to the Chinese market. Second, that comedy guy that follows Goldblum around was useless and annoying, even Gainsbourg could have been replaced by just having Goldblum talk more. And thirdly, Brent Spiner did a fine job as Doctor Okun, but while watching the first one, who in the hell thought we needed more of him?? Like every time he was on screen my hand was hovering over the skip button because I just did not care. He is not only the smartest super genius ever, but also has premonitions that allow him to know anything the plot needs him to.

			<br><br>

			Overall, I just felt that this entire film was following in the same footsteps as the original, only without the heart. Kind of similar to how The Force Awakens was a re-tread of A New Hope, only The Force Awakens was still fun. Independence Day has the enemy ships coming in over the moon, theres the cocky fighter pilot that takes the fight to them, the human's initial strike was a total failure, only for them to re-group, discover the alien's weakness and strike back disabling their entire fleet in one fell swoop. Also a major character commits heroic suicide by flying into the alien ship, this time as Pullman. And oh boy did I eye roll when William Fitchner was delivering a radio transmission to the entire world, AGAIN. 

			<br><br>

			I was hoping for a fun space action destruction adventure, but unfortunately there was no fun to be had here. Emmerich just re-made his masterpiece to cash in another buck with overseas audiences who look exclusively for big shiny new effects. For me, the real heroes of this movie are the unrecognized VFX artists who gave their lives so we could have this schlock.


				




            
            
