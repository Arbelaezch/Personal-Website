          <p>
			 
				The Favourite is a film by oddball director Yogos Lanthimos. It stars Olivia Colman, Emma Stone, and Rachel Weisz as three strong female characters all vying for eachothers' affections. The film garnered critical acclaim, receiving ten Oscar nominations, including Best Picture. Olivia Colman won Best Actess.

				<br><br>

				Off the bat I want to mention my initial aversion to the Favourite. I was not a fan of Yorgos' prior 2015 film The Lobster, and due to the wacky first ten minutes of The Favourite I expected to equally dislike it. Not because I don't think Yorgos is a talented director, but because his style is a level of absured that just does not resonate with me. Imagine my surprise when I actually ended up falling in love with the triangle of female protagonists and their rivalry.


				<br><br>

				The greatest success of The Favourite is by balancing its three female leads and having them pursue their desire for affection and acceptance in their own justified ways. The rivalry between Stone and Weisz was exciting to watch and it wasn't untill the end I was able to conclusively determine for myself which I liked more; not that that was the point. I appreciated that each character brought their own ambitions and methods of achieving those ambitions to the table. Colman especially who starts out a helpless mess and eventually shows she herself is more capable than anyone gives her credit for. 

				<br><br>


				I might as well mention the acting. It's good. Colman is obviously fantastic and had me laughing more than a couple times. Weisz was my second favorite performance and Stone did a good job herself playing the deceitful wolf in sheep's clothing. In addition to the acting, the cinematography was often quite beautiful and colorful, I quite enjoyed admiring certain frames from this film. The fisheye lense was initially jaring to me, but I got used to it eventually and it oddly fit the tone of the drama.

				<br><br>

				If I have any complaints at all, Yorgos' ecclectic editing and style are still a little much for my tastes. I can appreciate some wacky styles but for some reason I just cannot connect with Yorgos. I also didn't find the film all that funny. The drama was more than enough, and some of the absurdity did make me chuckle, but for the most part the comedy was lost on me.

            
            	<br><br>


				Overall, this is an excellently well made film that was well worth the watch and I expect to see it again to further break down the protagonists' characters and enjoy the wild rivalry that they engage in.



            
            
          </p>  
