          <p>
            
            <em>Train to Busan</em> was a massively successful South Korean zombie flick that has been praised since release as a fresh take on the tired genre. It incorporates World War Z style zombie effects along with an emotionally engaging story and characters.
           
			
			
			
			    <br><br>

				 Online it is frequently lauded as "the greatest zombie film of all time," and I may have to agree. Along with the original zombie film, Romero's <em>Night of the Living Dead,</em> I think Busan is a smash that certainly sticks out in the overpopulated genre. It employs solid effects, good acting, enough thrills to keep audiences hooked and an emotionally engaging story to boot. A collection of elements that is surprisingly difficult to find with zombie flicks. It misses out on the massive set pieces of a big Hollywood film, but the claustrophobia of the train cars provides a source of tension that more than makes up for it.

            
          

				<br><br>
				
				I had fun with <em>Busan</em> and going forward it'll be easy to recommend to friends looking for a solid zombie film experience.

          
        
            
