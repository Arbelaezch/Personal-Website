          <p style="text-align: center; text-indent: -.25in; margin: 30px 1in;"> 
            Fun Fact: A refugee from Germany who had worked in the Nazi Ministry of Culture told Chaplin that Hitler had watched the movie twice, entirely alone both times. Chaplin replied that he would "... give anything to know what he thought of it."<sup>1</sup>
          </p>

          <p>
          
            By the time of <em>The Great Dictator</em> Charlie Chaplin was a cultural icon who could do no wrong. So, being on top of the world, why not make his first talkie a political satire humiliating some of the most powerful leaders in the world at the time. Luckily for him it worked out and is known as a cinematic classic that garnered five Academy Award nominations.
            
            <br><br>



            <em> Dictator</em> is a beautiful yet solemn picture to revisit today. It marks a turning point in Charlie Chaplin's long and unprecedented career. At the time Chaplin was becoming more and more politically opinionated and was consumed with the parallels between Hitler and himself. They were born only four days apart, they were both born into poverty and rose to some of the most prominent cultural positions in the world, and they physically resembled each other down to their tiny little moustaches. It was these similarities, as well as a scathing critique of his character by the Nazi's in their book: <em>The Jews are Looking at You</em> (1934), that propelled Chaplin to make this film. Following <em>Dictator</em>, Chaplin's political opinions became so loud that the FBI ran a smear campaign on him, turning the American people against him. He would never again reach the critical or box office acclaim he had so consistently achieved for decades.
            
            <br><br>
            
            <em>Dictator</em> was a highly controversial film to make since at the time the United States was still neutral in the War and a comedy about Hitler somehow seemed like a crazy idea. His passion for the project drove him to ignore this, however, and Chaplin put it all on the line nearly bank rolling the entire $1.5 million himself. This was an extremely risky move because not only was it one of his most expensive pictures, but he also wouldn't be able to release it to a number of international markets like occupied Europe and fascist South American states due to its subject matter. Fortunately despite these restrictions, it still became Chaplin's most financially successful film and was well regarded critically despite it being a war time comedy.
            
            <br><br>

            I personally found <em>Dictator</em> to be not as funny or as thrilling as some of his previous works, but I guess thats because Chaplin's focus has shifted to providing a more direct commentary on his intended subject. Much of the comedy does come from the cartoonish portrait of Hitler, which has maybe lost some of its luster since he has been attacked and reimagined a million times in the last hundred years. That being said Chaplin's commentary is still very poignant and relevant in today's ever more politically charged world. And while it may not be as funny as it once was, you have to appreciate how Chaplin made a film people thought was crazy and tried to reach people in an honest way. His final speech is beautiful and a change of pace from Chaplin pictures that I whole heartedly welcomed, even if people of the day were displeased with it. <em>The Great Dictator</em> is Chaplin's most ambitious project that tackles comedy, politics, satire, and what it is to be human all in two hours. He was a genius ahead of his time and it is an absolute crime that society turned on him after this picture, depriving the world of his magnificent vision.



        
  
 
				 
