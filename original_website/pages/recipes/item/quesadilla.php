      <div class="method">
        <h2>Method</h2>
        <p>
          <h4>Chicken Prep</h4>
          <ol>
            <li>Start by butterflying your chicken breasts.</li>
            <li>Place them between sheets of plastic wrap and pound them out flat.</li>
            <li>Chicken Marinade: In a bowl combine 2 Tbsp vegetable oil, 2 crushed cloves of garlic, 1 tsp oregano, 1 Tbsp cumin, 1 tsp cayenne, 1 tsp smoked paprika, 1 tsp white sugar, juice of 1 lime, 1 Tbsp olive oil, salt and pepper. Whisk to combine.</li>
            <li>Place your chicken in a ziploc bag along with your marinade. Get all the air out and zip shut. Massage marinade to ensure an even coating.</li>
            <li>Place in a bowl and refrigerate for 30 minutes and up to 4 hours. </li>
            <li>Preheat skillet over high heat with some vegetable oil. Remove chicken from ziploc and cook until the internal temperature reaches 155°F. </li>
            <li>Remove from heat, let cool, and then slice.</li>
            <li>OPTIONAL: In the same skillet cook some peppers and onions in a little bit of vegetable oil. Cook until soft, and set aside.</li>
            
          </ol>
          <h4>Putting it Together</h4>
          <ol>
            <li>Grate a metric butt-ton of monterey jack and sharp cheddar cheese at a ratio of 3:2. Set aside.</li>
            <li>Preheat your pan over medium heat.</li>
            <li>Add chicken to a flour tortilla, top with peppers, onions, and cheese. Fold in half and place in a skillet over medium heat with some vegetable oil.</li>
            <li>Flip after 1 - 1 ½ minutes, when it’s golden brown and then cook the other side for 1 - 1 ½ minutes as well. </li>
            <li>Remove from heat, slice up, serve and enjoy! :)</li>
            
          </ol>
        </p>
