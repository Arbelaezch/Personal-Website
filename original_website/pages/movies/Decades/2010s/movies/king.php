          <p>
			 
				The King is a Netflix adaptation of Henry IV and Henry V by William Shakespeare, ever heard of him? It sees rising star, Timothée Chalamet, claim his father's thrown and struggle to rule as a young man king. The film, along with the plays it's based on, has been heavily criticised for its historical inaccuracies.
            
            <br><br>

				I very much enjoed The King. The political paranoia and isolation felt by Chalamet struck a cord with me that few political dramas do. As much as I hate to say it, I think that is because of Chalamet and his incredible performance here. His method across all of his films is subtle, and I've written him off before, but here his talent is on full display. Between repping an English accent as though he was native to it, and then speaking fluent French when needed, I was very impressed and it made the character that much more believable. Compare Timothee then to Pattinson, who did well as a Frenchman, but his accent slipped up occasionally and he also did not speak any French. I'm not necessarily saying this makes Chalamet a better actor, but these are two areas where Chalamet was able to bring his character to life better than the exceptionally talented Pattinson.

				<br><br>

				The King's production and costumes were also top notch. Netflix productions have a tendency to come off as cheap on occasion, and this was one project that they absolutely killed. There wasn't a single moment I was taken out of the world that director Michôd was crafting.

				<br><br>

				Story-wise, The King kept me engaged and anxious all the way through. It didn't really do anything new in terms of political dramas nor medievel action films. What it did do though, it did very well. It had you connecting to its characters and motivations in a way that made me hope to see them succeed and what more can you ask of a very good film than that.






            
            
