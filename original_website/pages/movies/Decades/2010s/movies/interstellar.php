          <p>
			 
			Interstellar is a 2014 sci-fi masterpiece directed by film legend and Reddit's heart-throb, Christopher Nolan. The film won the Academy Award for Visual Effects and was nominator for Score and Production Design as well as Sound Mixing/Editing. The film was a critical and box office success with a projected profit of forty-two million dollars. It now also rests at an astonishing #29 on IMDB's top 250.

			<br><br>

			I've very much had a love hate relationship with Interstellar. At first I loved it for its blend of emotionally engaging characters and incredible true to life science. Then I hated it for its seeming abandonment of that strict adherence to scientific accuracy in favor of typical Hollywood sentimentality (granted I was also peeved by it garnering a top thirty spot on IMDB). And now in my ripe old age, after my fourth go around, I can honestly say I have fallen back in love with this Nolan masterpiece. Though, I have not forgiven some of my previous qualms.
            
			<br><br>

			Interstellar is a successful blockbuster genre film. Many sci-fi's in cinema usually tend to find themselves as either cerebral interpretations of our modern day world through the lense of technology, or as fun space adventures that provide us with some quick and easy escapism. Interstellar, unlike others, straddles the line between these two points. It does this by establishing emotionally rich characters that ground audiences in this incredibly bleak tale. It also pushes audiences to appreciate miraculous scientific phenomenon by building its action around them. This is of course encompassed by incredible acting performances as well as spectacular visual effects that allow for this movie's writing to be showcased as good as it possibly can be. All these elements together make Interstellar a highly enjoyable, accessible experiece that also manages to push the cerebral nature of its sci-fi theme that gives audiences a reason to think on what the film is trying to say to them.

			<br><br>

			Unfortunately, beyond the basic "cool space lessons," this movie is not saying much. Okay maybe that's unfair. It is trying to get you to question the nature of love and humanity amongs all other natural observations. I, personally, just could not get into this. To me, the way Nolan leads into having Love be the thing that transcends dimensions was just too phony. And don't get me wrong, I'm all for discussions of our own humanity in unexpected places in film. Like Blade Runner I feel handled the discussion of humanity within machines very well to the point where I was fully on board by the end of that film. My problem with Interstellar is that the film focuses so heavily on scientific principles and accuracy, and features only a single conversation of Anne Hathaway questioning our perceptions of love, before going total balls to the walls "love conquers all." To me this felt incredibly cheap and completely undercuts the rest of the film. Maybe if there was a more steady lead up of arguments to the reveal that love rocks, I would be able to stomach it easier. This of course does not ruin the film for me, anymore, though it is the reason I personally cannot consider it one of the best films of all time. 

			<br><br>

			The only other detail I can nitpick is that McConaughey's son should not have been in this movie at all he was so useless. 

			<br><br>

			As for the rest of the good however: the water planet was sick, I thought Matt Damon was utilized perfectly, John Lithgow is awesome as was young Murph, the use of time relativity was wildly enjoyable, and goddamn was the wormhole segment wild.

			<br><br>

			I guess what I'm trying to say is that Interstellar is a really really good movie, maybe even a masterpiece. I will hopefully enjoy watching it for years to come. It just dives full-on into typical Hollywood sentimentality too jarringly, so much so that I am regularly taken out of it each time I return.

			<br><br>

            
            
