          <p>
			 
				2001 is the eighth film by legendary filmmaker Stanley Kubrick. It is a mostly non-verbal sci-fi experience that addresses artificial intelligence, existentialism, non-human super beings and evolution. It won the Academy Award for Visual Effects and over its many re-releases has grossed approximately $150 million. It is widely considered one of the greatest films of all time, though ever since its release general audiences have been split on it.
            
            <br><br>

				Visually the film is phenomenal. With 2001, Kubrick made space look and feel real, something that had never been done before. He achieved this practically with absolutely zero computer generated special effects in the entire film. Kubrick also pioneered the use of front projection with retroflective matting to have actors realistically look like they were on the moon. It's harder to appreciate a film when the quality of its effects have since become commonplace, but yet it is important to ascribe the film the credit it is due considering more than fifty years later it still looks incredible.

				<br><br>

				Writing-wise, the film stumbles. It addresses emormous concepts like existentialism and evolution with ease in a nearly entirely non-verbal way. Rather than teaching these ideas to the viewer, it impresses them upon audiences in a way that they can feel. In this, the film achieves Kubrick's goal of finding a timeless and universal way of conveying complex concepts. On the flip side, the human dialogue is largely trivial and leads to very little connection between audience member and character. This can leave already bored audiences feeling even more bored at the lack of emotional connect to the human element of the story. However, I believe the vanilla dialogue was used as a way to further drive home the development and simple existence of humanity in a far more complex and exciting universe.

				<br><br>

				2001 is very much an experience, rather than necessarily a story to enjoy. You need to allow yourself to feel it and what it is trying to say instead of following along a traditional spoken narrative. With 2001, Kubrick achieved a pioneering feat in both sci-fi as well as general film storytelling. 





            
            
