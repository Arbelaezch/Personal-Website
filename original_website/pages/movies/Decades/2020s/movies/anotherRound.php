          <p>
			 
				Another Round is a Danish film starring Mads Mikkelsen about a complacent middle aged man rediscovering his life through the powers of alcohol.
            
            <br><br>

				I wanted to like this film more than I did. I feel like the concept is such a slam dunk. An experiment with alcohol to enjoy the day to day of your life more seems like a concept that is not only exciting but applicable to modern day (or just general) complacency. Instead of taking an honest look at the possible benefits and detriments of alcohol in daily living, the film makes a very one dimensional and irrational case against being drunk all day.

				<br><br>

				I really do feel there was an opportunity to explore this "alcoholic theory" in this film, and display real life consequences of it. Instead, the film takes the very obvious route of having the characters go way too far with it and having an obvious answer to why alcohol is bad. "Alcohol kills." Maybe I was too hopeful about getting a more insightful conversation about the topic. I'm always a big fan of films where the characters rediscover the beauty in their lives, but for me this one was too contrived and obvious to take seriously.
				
				<br><br>

				It's not all bad. Obviously Mikkelson does great. The cinematography is standard. The concept and execution are actually pretty entertaining for the most part. I'm probably just more butthurt at the missed opportunity for greater conversation. It is a fun film, and the main song is catchy as all hell. Though it may not have lived up to my expectations, it is still worth checking out, though don't expect to be surprised at all by the film's take on alcohol.






            
            
          </p>  
